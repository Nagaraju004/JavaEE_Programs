package dao;

public class employee {
	
	private int id;
	private String name;
	private int age;
	private String city;
	
	
	public int getid()
	{
		return id;
	}
	public String getname()
	{
		return name;
	}
	public int getage()
	{
		return age;
	}
	public String getcity()
	{
		return city;
	}
	
	public void setid(int id)
	{
		this.id=id;
	}
	public void setname(String name)
	{
		this.name=name;
	}
	public void setage(int age)
	{
		this.age=age;
	}
	public void setcity(String city)
	{
		this.city=city;
	}
	

}
